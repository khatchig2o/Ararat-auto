function setLanguage(lang) {
  for (const key in translations[lang]) {
    const el = document.getElementById(key);
    if (el) {
      // For multi-line text (like vipDesc), use innerText
      if (key === "vipDesc") {
        el.innerText = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  }
  localStorage.setItem("araratLang", lang);
}

// Language dropdown logic
document.addEventListener("DOMContentLoaded", () => {
  const switcher = document.querySelector(".lang-switcher");
  const selectedBtn = switcher.querySelector(".lang-selected");
  const langList = switcher.querySelector(".lang-list");
  const langLabel = document.getElementById("langLabel");
  const langGlobe = document.getElementById("langGlobe");

  // Map language code to label
  const labelMap = {
    en: "English",
    hy: "Armenian",
    ru: "Russian",
  };

  // Open/close dropdown
  selectedBtn.addEventListener("click", (e) => {
    e.stopPropagation();
    switcher.classList.toggle("open");
  });

  // Select language from dropdown
  langList.querySelectorAll("li").forEach((li) => {
    li.addEventListener("click", () => {
      const lang = li.getAttribute("data-lang");
      setLanguage(lang);
      langLabel.textContent = labelMap[lang];
      // Highlight selected in dropdown
      langList
        .querySelectorAll("li")
        .forEach((item) => item.classList.remove("selected"));
      li.classList.add("selected");
      switcher.classList.remove("open");
    });
  });

  // Close dropdown on outside click
  document.addEventListener("click", () => {
    switcher.classList.remove("open");
  });

  // Set initial label and highlight from localStorage
  const savedLang = localStorage.getItem("araratLang") || "en";
  langLabel.textContent = labelMap[savedLang];
  langList.querySelectorAll("li").forEach((li) => {
    li.classList.toggle("selected", li.getAttribute("data-lang") === savedLang);
  });
});
